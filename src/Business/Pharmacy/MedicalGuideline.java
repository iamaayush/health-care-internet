package Business.Pharmacy;

/**
 *
 * @author Aayush
 */
public class MedicalGuideline {
    
private String usageInfo;
private String dosageInfo;
private String storageInfo;
private String sideEffects;
private String ingredients;
private String refillUse;
private String notForPeople;
private String keepOutOf;
private String others;

    public String getUsageInfo() {
        return usageInfo;
    }

    public void setUsageInfo(String usageInfo) {
        this.usageInfo = usageInfo;
    }

    public String getDosageInfo() {
        return dosageInfo;
    }

    public void setDosageInfo(String dosageInfo) {
        this.dosageInfo = dosageInfo;
    }

    public String getStorageInfo() {
        return storageInfo;
    }

    public void setStorageInfo(String storageInfo) {
        this.storageInfo = storageInfo;
    }

    public String getSideEffects() {
        return sideEffects;
    }

    public void setSideEffects(String sideEffects) {
        this.sideEffects = sideEffects;
    }

    public String getIngredients() {
        return ingredients;
    }

    public void setIngredients(String ingredients) {
        this.ingredients = ingredients;
    }

    public String getRefillUse() {
        return refillUse;
    }

    public void setRefillUse(String refillUse) {
        this.refillUse = refillUse;
    }

    public String getNotForPeople() {
        return notForPeople;
    }

    public void setNotForPeople(String notForPeople) {
        this.notForPeople = notForPeople;
    }

    public String getKeepOutOf() {
        return keepOutOf;
    }

    public void setKeepOutOf(String keepOutOf) {
        this.keepOutOf = keepOutOf;
    }

    public String getOthers() {
        return others;
    }

    public void setOthers(String others) {
        this.others = others;
    }



}
