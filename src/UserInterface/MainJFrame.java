    private void loginjButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginjButton1ActionPerformed
        // TODO add your handling code here:
        String userName = userNamejTextField1.getText();
        char[] passwordCharArray = passwordjPasswordField1.getPassword();
        String password = String.valueOf(passwordCharArray);

        UserAccount userAccount = system.getUserAccountDirectory().authenticateUser(userName, password);
        NetworkCountry inCountry = null;
        NetworkState inState = null;
        NetworkCity inCity = null;
        Enterprise inEnterprise = null;
        Organization inOrganization = null;

        if (userAccount == null) {

            userAccount = system.getCreator().getUserAccountDirectory().authenticateUser(userName, password);
            if (userAccount != null) {
                inEnterprise = system.getCreator();
            }

            if (userAccount == null) {

                for (Organization org : system.getCreator().getOrganizationDirectory().getOrganizationList()) {
                    userAccount = org.getUserAccountDirectory().authenticateUser(userName, password);

                    if (userAccount != null) {
                        inEnterprise = system.getCreator();
                        inOrganization = org;
                        break;
                    }

                }
            }

        }
        if (userAccount == null) {
            for (NetworkCountry networkCountry : system.getCountryNetworkList()) {
                for (NetworkState networkState : networkCountry.getStateList()) {
                    for (NetworkCity networkCity : networkState.getCityList()) {
                        for (Enterprise enterprise : networkCity.getEnterpriseDirectory().getEnterpriseList()) {
                            userAccount = enterprise.getUserAccountDirectory().authenticateUser(userName, password);
                            if (userAccount == null) {
                                for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()) {
                                    userAccount = organization.getUserAccountDirectory().authenticateUser(userName, password);
                                    if (userAccount != null) {
                                        inCountry = networkCountry;
                                        inState = networkState;
                                        inCity = networkCity;
                                        inEnterprise = enterprise;
                                        inOrganization = organization;
                                        break;
                                    }
                                }
                            } else {
                                inEnterprise = enterprise;
                                break;
                            }
                            if (inOrganization != null) {
                                break;
                            }
                        }
                        if (inEnterprise != null) {
                            break;
                        }
                    }
                    if (inEnterprise != null) {
                        break;
                    }
                }
                if (inEnterprise != null) {
                    break;
                }
            }
        }

        if (userAccount == null) {
            JOptionPane.showMessageDialog(null, "Invalid credentials");
        } else {
            CardLayout layout = (CardLayout) container.getLayout();
            container.add("WorkArea", userAccount.getRole().createWorkArea(container, userAccount, inOrganization, inEnterprise, system));
            layout.next(container);
        }
    }//GEN-LAST:event_loginjButton1ActionPerformed

