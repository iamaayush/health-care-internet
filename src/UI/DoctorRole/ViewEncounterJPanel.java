    private void populateTable() {

        DefaultTableModel dtm = (DefaultTableModel) encounterjTable1.getModel();
        dtm.setRowCount(0);

        Object[] row = new Object[2];
        for (Encounter enc : encounterList) {
            row[0] = enc;
            row[1] = enc.getEncounterDate();

            dtm.addRow(row);
        }
    }

