        int selectedRow = encounterjTable1.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "select a row");
            return;
        }
        
        Encounter encounter = (Encounter)encounterjTable1.getValueAt(selectedRow, 0);
        ViewEmergencyEncounterDetailsJP veedjp = new ViewEmergencyEncounterDetailsJP(userProcessContainer,encounter);
        userProcessContainer.add("ViewEmergencyEncounterDetailsJP", veedjp);
