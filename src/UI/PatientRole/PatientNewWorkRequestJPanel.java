    private void populateDoctorTable() {
        
        DefaultTableModel model = (DefaultTableModel) doctorJTable.getModel();
        model.setRowCount(0);

        Object[] row = new Object[4];
        row[0] = patient.getPrefferedDoctor().getDoctorLiscenceID();
        row[1] = patient.getPrefferedDoctor();
        row[2] = patient.getPrefferedDoctor().getSpeciality();
        row[3] = patient.getEntPrefDoc();
        model.addRow(row);

        if (patient.getReferredDoctor() != null) {
            Object[] row2 = new Object[4];
            row2[0] = patient.getReferredDoctor().getDoctorLiscenceID();
            row2[1] = patient.getReferredDoctor();
            row2[2] = patient.getReferredDoctor().getSpeciality();
            row2[3] = patient.getEntreffDoc();
            model.addRow(row2);

        }
    }

