    private void sendjButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sendjButton1ActionPerformed
        // TODO add your handling code here:
        String message = messagejTextField1.getText();

        int selectedRow = doctorJTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "select a row");
            return;
        }
        
        Doctor doctor = (Doctor) doctorJTable.getValueAt(selectedRow, 1);
        enterprise = (Enterprise) doctorJTable.getValueAt(selectedRow, 3);

        doctor.getPatientList().add(patient);

        PatientWorkRequest request = new PatientWorkRequest();
        request.setPatient(patient);
        request.setMessage(message);
        request.setDoctor(doctor);
        request.setRequestDate(new Date());
        request.setEntRecv(enterprise);
        request.setStatus("sent");
        request.setAppointment("pending");

        patient.getWorkQueue().getWorkRequestList().add(request);
        doctor.getWorkQueue().getWorkRequestList().add(request);

    }//GEN-LAST:event_sendjButton1ActionPerformed

