    private void submitjButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitjButton1ActionPerformed
        // TODO add your handling code here:

        int selectedRow = drugjTable1.getSelectedRow();

        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Select a Row.");
            return;
        }
        DrugMngrToFDAWorkRequest dmr = (DrugMngrToFDAWorkRequest) drugjTable1.getValueAt(selectedRow, 4);

        if (approvedissapprovejComboBox1.getSelectedItem().equals("Approve")) {
            dmr.getDrug().setFdaStatus("Approved");

        } else if (approvedissapprovejComboBox1.getSelectedItem().equals("Reject")) {
            dmr.getDrug().setFdaStatus("Rejected");
        }

        populateTable();

    }//GEN-LAST:event_submitjButton1ActionPerformed

    private void viewDrugjButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewDrugjButton1ActionPerformed
        // TODO add your handling code here:
        int selectedRow = drugjTable1.getSelectedRow();

        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Select a Row.");
            return;
        }
        DrugMngrToFDAWorkRequest dmr = (DrugMngrToFDAWorkRequest) drugjTable1.getValueAt(selectedRow, 4);
        Drug drug = (Drug) dmr.getDrug();
        ViewDrugJPanel vdjp = new ViewDrugJPanel(userProcessContainer, drug);
        userProcessContainer.add("ViewDrugJPanel", vdjp);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
    }//GEN-LAST:event_viewDrugjButton1ActionPerformed

