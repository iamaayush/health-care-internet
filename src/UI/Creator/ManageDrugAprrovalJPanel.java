    private void populateTable() {
        DefaultTableModel dtm = (DefaultTableModel) drugjTable1.getModel();

        dtm.setRowCount(0);

        for (WorkRequest request : drugRegOrg.getWorkQueue().getWorkRequestList()) {
            DrugMngrToFDAWorkRequest dmr = (DrugMngrToFDAWorkRequest) request;
            Object[] row = new Object[6];
            row[0] = dmr.getEntId();
            row[1] = dmr.getEnterpriseName();
            row[2] = dmr.getDrug().getDrugID();
            row[3] = dmr.getDrug();
            row[4] = dmr;
            row[5] = dmr.getDrug().getFdaStatus();

            dtm.addRow(row);
        }
    }

