    private void registerjButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerjButton1ActionPerformed
        // TODO add your handling code here:
        try {
            String name = namejTextField1.getText();
            float iCDCode = Float.parseFloat(ICDCodejTextField2.getText());
            String symptoms = symptomsjTextArea1.getText();
            String desc = descjTextArea2.getText();

            if ((name == null) || (ICDCodejTextField2.getText() == null)
                    || (symptoms == null) || (desc == null)) {
                JOptionPane.showMessageDialog(null, "Field(s) cannot be empty.");
                return;
            }

            for (Disease d : iCDOrg.getWorldDiseaseList()) {
                if (iCDCode==(d.getiCDCode())) {
                    JOptionPane.showMessageDialog(null, "A disease is already registered on this ICD Code.");
                    return;
                }
            }

            Disease disease = new Disease();
            disease.setDiseaseName(name);
            disease.setSymptoms(symptoms);
            disease.setiCDCode(iCDCode);
            disease.setDescription(desc);

            iCDOrg.getWorldDiseaseList().add(disease);
        } catch (NumberFormatException nfe) {
            JOptionPane.showMessageDialog(null, "Enter valid values");
            return;
        }

        populateTable();
    }//GEN-LAST:event_registerjButton1ActionPerformed

