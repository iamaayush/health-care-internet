    private void processjButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_processjButton1ActionPerformed
        // TODO add your handling code here:

        int selectedRow = drugjTable1.getSelectedRow();

        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Select a Row.");
            return;
        }

        Drug drug = (Drug) drugjTable1.getValueAt(selectedRow, 3);
        if (enterprise instanceof PharmacyEnterprise) {
            PharmacyEnterprise pe = (PharmacyEnterprise) enterprise;
            pe.getDrugList().remove(drug);
        }
        populateTable();
    }//GEN-LAST:event_processjButton1ActionPerformed

