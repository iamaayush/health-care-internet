    private void populateTable() {
        DefaultTableModel dtm = (DefaultTableModel) drugjTable1.getModel();

        dtm.setRowCount(0);

        for (WorkRequest request : enterprise.getWorkQueue().getWorkRequestList()) {
            if (request instanceof DrugRollBackWorkRequest) {
                DrugRollBackWorkRequest drwr = (DrugRollBackWorkRequest) request;
                Object[] row = new Object[6];
                row[0] = drwr.getEnterprise().getOrganizationID();
                row[1] = drwr.getEnterprise();
                row[2] = drwr.getDrug().getDrugID();
                row[3] = drwr.getDrug();
                row[4] = drwr;
                row[5] = drwr.getDrug().getFdaStatus();

                dtm.addRow(row);
            }
        }
    }

