    private void addjButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addjButton1ActionPerformed
        // TODO add your handling code here:
        String name = stateNamejTextField1.getText();
        if (name.matches(""))
        {
            JOptionPane.showMessageDialog(null,"Field cannot be left empty.");
            return;
        }
        for(NetworkState networkState : networkCountry.getStateList())
        {
            if (name.equalsIgnoreCase(networkState.getStateName()))
            {
                JOptionPane.showMessageDialog(null, "This State already exists.");
                return;
            }
        }
        
        stateNamejTextField1.setText(null);

        NetworkState networkState = networkCountry.createAndAddState();
        networkState.setStateName(name);

        populateStateTable();
    }//GEN-LAST:event_addjButton1ActionPerformed

    private void deletejButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletejButton2ActionPerformed
        // TODO add your handling code here:
        int row = statejTable1.getSelectedRow();
        if(row<0) {
            JOptionPane.showMessageDialog(null, "select a row");
        }
        else {
            int selectedValue = JOptionPane.showConfirmDialog(null, "Are you Sure you want to Delete?", "WARNING", JOptionPane.YES_NO_OPTION);
        
        if (selectedValue == JOptionPane.YES_OPTION)
        {
            NetworkState networkState = (NetworkState)statejTable1.getValueAt(row, 0);

            networkCountry.removeState(networkState);
            populateStateTable();
        }
         else{
            return;
        }}
    }//GEN-LAST:event_deletejButton2ActionPerformed

     private void populateStateTable()
    {
        DefaultTableModel model = (DefaultTableModel)statejTable1.getModel();
        model.setRowCount(0);
        
        for(NetworkState networkState: networkCountry.getStateList())
        {
            Object[] row = new Object[1];
            row[0]=networkState;
            model.addRow(row);
        }
    }
    
    private void addCityjButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addCityjButton1ActionPerformed
        // TODO add your handling code here:

        int row = statejTable1.getSelectedRow();
        if(row<0) {
            JOptionPane.showMessageDialog(null, "select a row");
        }
        else {
        NetworkState networkState = (NetworkState)statejTable1.getValueAt(row, 0);
        AddCityJPanel acjp = new AddCityJPanel(userProcessContainer,networkState);
        userProcessContainer.add("addCityJPanel",acjp);
        CardLayout layout = (CardLayout)userProcessContainer.getLayout();
        layout.next(userProcessContainer);
        }
    }//GEN-LAST:event_addCityjButton1ActionPerformed

